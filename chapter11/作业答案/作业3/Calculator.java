
public class Calculator {

	int num1;  //运算数1
	int num2;  //运算数2
	
	/**
	 * 加法
	 * @return num1+num2
	 */
	public int add(){
		return (num1 + num2);
	}
	/**
	 * 减法
	 * @return num1-num2
	 */
	public int minus(){
		return (num1 - num2);
	}

	/**
	 * 乘法
	 * @return num1*num2
	 */
	public int multiple(){
		return (num1 * num2);
	}
	/**
	 * 除法
	 * @return num1/num2
	 */
	public int divide(){
		return (num1/num2);
	}
}
