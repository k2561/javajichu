package cn.jbit.classandobject;
/**
 * 计数器类
 */
public class Calculator {
	int num1;//整数1
	int num2;//整数2
	
	//加法
	public int add(){
		return num1+num2;
	}
	//减法
	public int sub(){
		return num1-num2;
	}
	//乘法
	public int multi(){
		return num1*num2;
	}
	//除法
	public int div(){
		return num1/num2;
	}
}
